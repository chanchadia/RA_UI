  export default function getColor (value)
  {
    if(value === 1)
      return '#00B050';
    if(value === 2)
      return '#00B0F0';
    if(value === 3)
      return '#FFFF00';
    if(value === 4)
      return '#FFC000';
    if(value === 5)
      return '#FF3B37';
    return null;
  }

  export const tableHeaderBgColor = '#eff0e9ff';